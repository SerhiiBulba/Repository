### **** Client-Server, HTTP, API ******
## Client - the role which sends the request
## Server - the role which accepting the request

## Each REQUEST contains from:
# -> Request (resourse URL, protocol version - HTTP 1.0, Method - GET, POST, UPDATE, DEL)
# -> Header (device type, auth info, header accepted language, etc)
# -> Body

## Each RESPONSE contains from:
# -> Response ( protocol version - HTTP 1.0, status code, status message)
# -> Header (device type, auth info, header accepted language, etc)
# -> Body